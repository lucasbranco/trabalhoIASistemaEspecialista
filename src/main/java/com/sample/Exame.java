package com.sample;

public class Exame {

	private int renal;
	private int urina;
	private double creatinina;
	private int clearance;
	
	public int getRenal() {
		return renal;
	}
	public void setRenal(int renal) {
		this.renal = renal;
	}
	public int getUrina() {
		return urina;
	}
	public void setUrina(int urina) {
		this.urina = urina;
	}
	public double getCreatinina() {
		return creatinina;
	}
	public void setCreatinina(double creatinina) {
		this.creatinina = creatinina;
	}
	public int getClearance() {
		return clearance;
	}
	public void setClearance(int clearance) {
		this.clearance = clearance;
	}
}
